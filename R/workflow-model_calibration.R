##
## Epidemic Model Parameter Calibration, HPC setup
##

# Setup ------------------------------------------------------------------------
library(slurmworkflow)
library(EpiModelHPC)
source("R/000-project_settings.R")

hpc_configs <- swf_configs_rsph(
  partition = "preemptable",
  mail_user = mail_user
)

hpc_configs <- swf_configs_hyak(
  hpc = "klone",
  partition = "ckpt",
  r_version = "4.1.0",
  mail_user = mail_user
)

max_cores <- 30

# Workflow creation ------------------------------------------------------------
wf <- create_workflow(
  wf_name = "model_calibration",
  default_sbatch_opts = hpc_configs$default_sbatch_opts
)

# Update RENV on the HPC -------------------------------------------------------
wf <- add_workflow_step(
  wf_summary = wf,
  step_tmpl = step_tmpl_renv_restore(
    git_branch = current_git_branch,
    setup_lines = hpc_configs$r_loader
  ),
  sbatch_opts = hpc_configs$renv_sbatch_opts
)

# Run the simulations ----------------------------------------------------------
library(EpiModelHIV)

epistats <- readRDS(fs::path(estimates_dir, "epistats.rds"))
netstats <- readRDS(fs::path(estimates_dir, "netstats.rds"))
est      <- readRDS(fs::path(estimates_dir, "netest.rds"))


param <- param.net(
  data.frame.params = readr::read_csv(fs::path(inputs_dir, "params.csv")),
  netstats          = netstats,
  epistats          = epistats,
  prep.start        = prep_start,
  riskh.start       = prep_start - 53,
  .param.updater.list = list(
    # High PrEP intake for the first year; go back to normal to get to 15%
    list(at = prep_start, param = list(prep.start.prob = function(x) x * 2)),
    list(at = prep_start + 52, param = list(prep.start.prob = function(x) x/2))
  )
)

init <- init_msm(
  prev.ugc = 0.05,
  prev.rct = 0.05,
  prev.rgc = 0.05,
  prev.uct = 0.05
)

# Controls
source("R/utils-targets.R")
control <- control_msm(
  nsteps              = calib_end,
  nsims               = 1,
  ncores              = 1,
  cumulative.edgelist = TRUE,
  truncate.el.cuml    = 0,
  .tracker.list       = calibration_trackers,
  verbose             = FALSE,
  .checkpoint.dir     = "temp/cp_calib",
  .checkpoint.clear   = TRUE,
  .checkpoint.steps   = 15 * 52
)

# insert test values here
scenarios.df <- tibble(
  .scenario.id = c("0"),
  .at = 1,
  hiv.trans.scale_1	= c(4.1),
  hiv.trans.scale_2	= c(.53),
  hiv.trans.scale_3	= c(.32)
)
scenarios.list <- EpiModel::create_scenario_list(scenarios.df)

wf <- add_workflow_step(
  wf_summary = wf,
  step_tmpl = step_tmpl_netsim_scenarios(
    est, param, init, control,
    scenarios_list = scenarios.list,
    output_dir = calibration_dir,
    libraries = "EpiModelHIV",
    n_rep = 150,
    n_cores = max_cores,
    max_array_size = 999,
    setup_lines = hpc_configs$r_loader
  ),
  sbatch_opts = list(
    "mail-type" = "FAIL,TIME_LIMIT",
    "cpus-per-task" = max_cores,
    "time" = "04:00:00",
    "mem" = "0" # special: all mem on node
  )
)

# Process calibrations ---------------------------------------------------------
# produce a data frame with the calibration targets for each scenario
wf <- add_workflow_step(
  wf_summary = wf,
  step_tmpl = step_tmpl_do_call_script(
    r_script = "R/wscript-calibration_process.R",
    args = list(
      ncores = 15,
      nsteps = 52
    ),
    setup_lines = hpc_configs$r_loader
  ),
  sbatch_opts = list(
    "cpus-per-task" = max_cores,
    "time" = "04:00:00",
    "mem-per-cpu" = "4G",
    "mail-type" = "END"
  )
)
